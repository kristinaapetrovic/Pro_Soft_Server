    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed

        String url = jTextFieldBaza.getText();
        String username = jTextFieldKorisnicko.getText();
        String pass = new String(jPasswordFieldLozinka.getPassword());
        String port = jTextFieldPort.getText();

        if (!validacija(url, username, pass, port)) {
            JOptionPane.showMessageDialog(this, "Proverite unete podatke", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Konfiguracija.getInstance().kreirajPrazanFajl();

        Konfiguracija.getInstance().setProperty("url", url);
        Konfiguracija.getInstance().setProperty("username", username);
        Konfiguracija.getInstance().setProperty("password", pass);
        Konfiguracija.getInstance().setProperty("port", port);

        Konfiguracija.getInstance().sacuvajIzmene();

        JOptionPane.showMessageDialog(this, "Podesavanje zavrseno, kada zatvorite formu server ce se pokrenuti!", "Uspesno", JOptionPane.INFORMATION_MESSAGE);

        this.dispose();

    }//GEN-LAST:event_jButtonSacuvajActionPerformed


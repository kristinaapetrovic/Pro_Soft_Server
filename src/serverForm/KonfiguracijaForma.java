    private boolean validacija(String url, String username, String pass, String port) {
        boolean urlReg = !url.isEmpty() && url.matches("^jdbc:mysql://localhost:\\d{1,5}/[a-zA-Z_][a-zA-Z0-9_]*$");
        boolean usernameReg = !username.isEmpty();
        boolean passReg = !pass.isEmpty();
        boolean portReg = !port.isEmpty() && port.matches("^\\d{1,5}$");

        if (urlReg && usernameReg && passReg && portReg) {
            return true;
        }

        if (!urlReg) {
            jTextFieldBaza.setBorder(new LineBorder(Color.RED, 2));
        }

        if (!usernameReg) {
            jTextFieldKorisnicko.setBorder(new LineBorder(Color.RED, 2));
        }

        if (!passReg) {
            jPasswordFieldLozinka.setBorder(new LineBorder(Color.RED, 2));
        }

        if (!portReg) {
            jTextFieldPort.setBorder(new LineBorder(Color.RED, 2));
        }

        return false;
    }
